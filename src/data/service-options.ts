export const ALL_SERVICE_OPTIONS = SERVICE_CATEGORIES.flatMap((group) => group.services);

export function normalizeServiceSearch(value: string): string {
  return value.trim().toLowerCase();
}

export function serviceMatchesQuery(value: string, query: string): boolean {
  const normalizedQuery = normalizeServiceSearch(query);
  if (!normalizedQuery) return true;
  return value.toLowerCase().includes(normalizedQuery);
}

export function filterServiceCategories(
  query: string,
  categories: readonly ServiceCategory[] = SERVICE_CATEGORIES,
): FilteredServiceCategory[] {
  const normalizedQuery = normalizeServiceSearch(query);
  if (!normalizedQuery)
    return categories.map((group) => ({ category: group.category, services: [...group.services] }));

  return categories.flatMap((group) => {
    const categoryMatches = group.category.toLowerCase().includes(normalizedQuery);
    const services = categoryMatches
      ? group.services
      : group.services.filter((service) => service.toLowerCase().includes(normalizedQuery));

    return services.length > 0 ? [{ category: group.category, services: [...services] }] : [];
  });
}

export function isExistingService(value: string): boolean {
  const normalizedValue = normalizeServiceSearch(value);
  if (!normalizedValue) return false;
  return ALL_SERVICE_OPTIONS.some((service) => service.toLowerCase() === normalizedValue);
}

export function getServiceCategoryCount(): number {
  return SERVICE_CATEGORIES.length;
}

export function getServiceOptionCount(): number {
  return ALL_SERVICE_OPTIONS.length;
}
